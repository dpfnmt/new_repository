//TCP和UDP的区别？
//tcp是面向字节流的，udp是面向数据报的
//tcp是面向链接的，udp是无链接的
//tcp保证数据包的正确性，udp可能丢包
//tcp保证数据的顺序性，udp不保证

//TCP为什么可以保证稳定传输
//因为TCP的序号保证它能够顺序到达，并且拥有流量控制以及拥塞控制的机制

//三次握手，四次挥手

//http和https的区别？
//http是超文本传输，信息透明话，https是具有安全性，具有加密传输的协议。
//两者也是使用不同的端口号，前者使用80，后者使用443
//加密方式：对称加密，非对称加密
//两者的区别？
//对称加密的速度快，非对称加密的速度慢
//对称加密是铭文传输，将公钥暴漏在外
//非对称加密将公钥暴露，只供客服端加密，服务器使用私钥加缪